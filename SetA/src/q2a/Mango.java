package q2a;

public class Mango {

	private String type;
	private int quality;
	private int price;
	
	public Mango() {

	}
	
	public Mango(String type, int quality, int price) {
		super();
		this.type = type;
		this.quality = quality;
		this.price = price;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getQuality() {
		return quality;
	}
	public void setQuality(int quality) {
		this.quality = quality;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
	
	
}
